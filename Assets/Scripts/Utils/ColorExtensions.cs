using UnityEngine;

namespace SIS {

    public static class ColorExtensions {

        /// <summary>
        /// Creates color with corrected brightness.
        /// </summary>
        /// <param name="color">Color to correct.</param>
        /// <param name="correctionFactor">The brightness correction factor. Must be between -1 and 1.
        /// Negative values produce darker colors.</param>
        /// <returns>
        /// Corrected <see cref="Color"/> structure.
        /// </returns>
        ///
        public static Color ColorWithBrightness(this Color color, float correctionFactor) {
            float red = (float)color.r * 255f;
            float green = (float)color.g * 255f;
            float blue = (float)color.b * 255f;

            if (correctionFactor < 0) {
                correctionFactor = 1 + correctionFactor;
                red *= correctionFactor;
                green *= correctionFactor;
                blue *= correctionFactor;
            } else {
                red = (255 - red) * correctionFactor + red;
                green = (255 - green) * correctionFactor + green;
                blue = (255 - blue) * correctionFactor + blue;
            }

            return new Color(red / 255f, green / 255f, blue / 255f, color.a);
        }
    }
}